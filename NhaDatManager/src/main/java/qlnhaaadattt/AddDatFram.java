    public static boolean luuangobject(ArrayList<ThongTinDat> ds, String duongdan)
    {
        try {
            FileOutputStream fo = new FileOutputStream(duongdan);
            ObjectOutputStream objc = new ObjectOutputStream(fo);
            objc.close();
            fo.close();
            
            return true;
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "loi file duong dan luu file");
            return false;
        }
    }
    
    
    
