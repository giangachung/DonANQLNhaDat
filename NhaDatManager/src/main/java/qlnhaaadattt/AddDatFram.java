    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       // String duongdan = "C:\\Java\\QLNhaDat\\QLNhaDat\\NhaDatManager\\src\\main\\java\\anhsododat\\tbnhan_c.JPG";
        String soThua = txtSoThua.getText();
        String dienTich = txtDienTich.getText();
        String tenDat  = cbbLoaiDat.getSelectedItem().toString();
        String giapRanh = txtGiapRanh.getText();
        String soDo = cbbSoDo.getSelectedItem().toString();
        String giaBan = txtGiaBan.getText();
        String nhanVienBan = txtNhanVienban.getText();
        String sDT = txtSDT.getText();
        String moTa = txtMoTa.getText();
        //String duongdan = tbAnh.getText();

        int maDat;
        if(tenDat.equals("dat tho cu")){
            maDat=01;
        }else if(tenDat.equals("dat ruong")){
            maDat=02;
        }else {
            maDat=03;
        }


        if (soThua.equals("") || dienTich.equals("") || tenDat.equals("")
                || giapRanh.equals("") ||  soDo.equals("")|| giaBan.equals("")
                ||nhanVienBan.equals("") || sDT.equals("") || moTa.equals("")) {
            Label lbErr = new Label();
            lbErr.setText("*Khong duoc de trong");
        } else {
            ThongTinDatDAO dao = new ThongTinDatDAO();
            ThongTinDat x = new ThongTinDat(Integer.parseInt(soThua)
                    ,Float.parseFloat(dienTich) , maDat,giapRanh,soDo
                    ,Float.parseFloat(giaBan),nhanVienBan, sDT,moTa);
//
            if (dao.add(x) == 1) {
                qldat.AddDat(x);

                JOptionPane a = new JOptionPane("Add thanh cong");
                 JOptionPane.showMessageDialog(rootPane, "them thanh cong!!!");
              //  jOptionPane1.showMessageDialog(null, "Thêm hồ sơ thành công!", "Success", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane a = new JOptionPane("Them ho so that bai");
               // jOptionPane1.showMessageDialog(null, "Thêm hồ sơ thất bại!", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

